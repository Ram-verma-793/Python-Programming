# Assignment operators
print("Assignment operators")

a = 10 # a assigned to 10
print(a)
a += 3 # a = a+3  now a is 10 and then 13
print(a)
a -= 3 # a = a-3 now a is 13 so it be 10
print(a)
a *= 2 # a = a*2
print(a)
a /= 2 # a = a/2
print(a)
a %= 2 # a = a%2
print(a)
a **= 2 # a = a**2
print(a)
a //= 2 # a = a//2
print(a)

